package EEP.App0.Util;

/**
 * A class that contains mathematical constants.
 * 
 */
public class Constants {
    /**
     * The constant PI is the ratio of a circle's circumference to its diameter.
     * 
     * @see "https://en.wikipedia.org/wiki/Pi"
     */
    public static final double PI = 3.1415926535;
    
    /**
     * The constant E is the base of the natural logarithm.
     * 
     * @see "https://en.wikipedia.org/wiki/E_(mathematical_constant)"
     */
    public static final double E = 2.71828182845;

    /**
     * Boltzmann's constant relates energy to temperature.
     * 
     * @see "https://en.wikipedia.org/wiki/Boltzmann_constant"
     */
    public static final double K = 1.38064852e-23;
}
